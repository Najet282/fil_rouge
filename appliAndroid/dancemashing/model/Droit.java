package com.example.dancemashing.model;

public class Droit {

    /*************************     ATTRIBUTS     ****************************/

    private int mIdDroit;
    private String mNom;

    /***********************     CONSTRUCTEUR     ***************************/
    public Droit() {
    }

    /**********************     GETTERS/SETTERS     *************************/

    public int getmIdDroit() {
        return mIdDroit;
    }

    public void setmIdDroit(int mIdDroit) {
        this.mIdDroit = mIdDroit;
    }

    public String getmNom() {
        return mNom;
    }

    public void setmNom(String mNom) {
        this.mNom = mNom;
    }

}
